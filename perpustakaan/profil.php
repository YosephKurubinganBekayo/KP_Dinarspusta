        <h3></h3>
        <p>Kegiatan perpustakaan Pemerintah Provinsi DKI Jakarta sudah dimulai sejak tahun 1950 ketika masih berbentuk kotapraja dengan sebutan Perpustakaan Kotapradja Djakarta Raja. Pada tahun 1961, setelah Kotapradja Djakarta Raja ditingkatkan statusnya menjadi Daerah Tingkat I DKI Djakarta Raja, kemudian menjadi Perpustakaan Balaikota Pemerintah DKI Djakarta Raja. Kemudian pada tahun 1972, melalui Keputusan Gubernur Kepala DKI Jakarta tentang Susunan Organisasi Sekretariat Daerah Pemerintah DKI Jakarta, Bagian Perpustakaan dibagi atas Sub Bagian Perpustakaan dan Sub Bagian Tata Usaha Perpustakaan, di mana perpustakaan merupakan salah satu bagian pada Biro Organisasi dan Ketatalaksanaan. Pada tahun 1978, melalui Keputusan Gubernur Kepala DKI Jakarta dibentuk Lembaga Perpustakaan Umum yang menangani jenis perpustakaan umum di lingkungan Pemerintah DKI Jakarta, seperti Perpustakaan Umum Gelanggang Mahasiswa Soemantri Brodjonegoro dan Perpustakaan Umum di lima wilayah Kotamadya DKI Jakarta.</p>
        <p>Pada tahun 1981 perpustakaan umum bernaung di bawah Biro Bina Mental dan Spiritual dengan status non struktural. Pada tahun 1989, perpustakaan umum di lima wilayah kotamadya DKI Jakarta dialihkan pengelolaannya kepada Dinas Pendidikan dan Pengajaran DKI Jakarta sebagai Unit Pelaksana Teknis Dinas (UPTD), sedangkan Perpustakaan Umum Soemantri Brodjonegoro masih tetap dikelola Biro Bina Mental Spiritual DKI Jakarta.</p>
